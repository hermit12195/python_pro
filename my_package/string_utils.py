def up(string):
    """
    Converts all characters in a string to uppercase.

    Parameters:
    string (str): The string to be converted to uppercase.

    Returns:
    str: The uppercase version of the input string.
    """

    return string.upper()

def remove_space(string):
    """
    Removes leading and trailing spaces from a string.

    Parameters:
    string (str): The string from which leading and trailing spaces will be removed.

    Returns:
    str: The string with leading and trailing spaces removed.
    """

    return string.strip()

